package org.openecomp.dcae.apod.analytics.model.facade.tca;

import lombok.Data;

/**
 * Response generated by TCA for VES Message
 *
 * @author Rajiv Singla . Creation Date: 11/9/2016.
 */
@Data
public class TCAVESResponse implements TCAFacadeModel {

    private static final long serialVersionUID = 1L;

    /**
     * Closed loop control name for response.
     */
    private String closedLoopControlName;

    /**
     * Version of response generated by TCA.
     */
    private String version;

    /**
     * Request identifier.
     */
    private String requestID;

    /**
     * Timestamp for closed loop alarm start.
     */
    private Long closedLoopAlarmStart;

    /**
     * Client for closed loop event.
     */
    private String closedLoopEventClient;

    /**
     * Target type.
     */
    private String targetType;

    /**
     * AAI
     */
    private AAI aai;

    /**
     * Target for response.
     */
    private String target;

    /**
     *
     */
    private String from;

    /**
     * Policy scope.
     */
    private String policyScope;

    /**
     * Policy name.
     */
    private String policyName;

    /**
     * Policy version.
     */
    private String policyVersion;

    /**
     * Closed loop event's status.
     */
    private String closedLoopEventStatus;
}
